def eval(fn,  ctx=''):
  costs = []
  for i in range(10):
    steps = 100
    t0 = time.perf_counter()
    for i in range(steps):
      z = fn()
    z.view(-1)[0].item()
    t1 = time.perf_counter()
    cost_us = (t1 - t0) / steps * 1e6
    costs += [cost_us]
  print(f'Operator ({ctx}), cost_us = {sorted(costs)[len(costs) // 2]:.2f}')

